def countTriplets(a, x):
    a.sort()
    n = len(a)
    c = 0
    for i in range(n - 2):
        l = i + 1
        r = n - 1
        while l < r:
            if a[l] + a[r] + a[i] < x:
                c += (r - l)
                print(i, l, r, (r - l))
                l += 1
            else:
                r -= 1
    return c

